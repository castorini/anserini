  public static boolean hasIndex(String indexName) {
    for (ShardedCollection collection : ShardedCollection.values()) {
      for (ShardInfo shard : collection.shards) {
        if (shard.indexName.equals(indexName)) {
          return true;
        }
      }
    }
    return false;
  }

  public static ShardInfo getIndex(String indexName) {
    for (ShardedCollection collection : ShardedCollection.values()) {
      for (ShardInfo shard : collection.shards) {
        if (shard.indexName.equals(indexName)) {
          return shard;
        }
      }
    }
    throw new IllegalArgumentException("Shard index name: " + indexName + " not found!");
  }

  public static ShardInfo[] getShardedIndex(String identifier) {
    for (ShardedCollection collection : ShardedCollection.values()) {
      if (collection.identifier.equals(identifier)) {
        return collection.shards;
      }
    }
    throw new IllegalArgumentException("No collection found for identifier: " + identifier);
  }
